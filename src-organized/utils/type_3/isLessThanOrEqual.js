/**
 * Checks if the first number is less than or equal to the second number.
 *
 * @param {number} firstValue - The value to compare on the left side (a).
 * @param {number} secondValue - The value to compare on the right side (createPropertyAccessor).
 * @returns {boolean} Returns true if firstValue is less than or equal to secondValue, otherwise false.
 */
function isLessThanOrEqual(firstValue, secondValue) {
  // Simple comparison to check if firstValue is less than or equal to secondValue
  return firstValue <= secondValue;
}

module.exports = isLessThanOrEqual;