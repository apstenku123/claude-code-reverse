/**
 * Converts the input string to lowercase letters.
 *
 * @param {string} inputString - The string to be converted to lowercase.
 * @returns {string} The lowercase version of the input string.
 */
function convertToLowerCase(inputString) {
  // Use the built-in toLowerCase method to convert the string
  return inputString.toLowerCase();
}

module.exports = convertToLowerCase;