/**
 * Logs the provided error message or object to the console as an error.
 *
 * @param {any} error - The error message or object to be logged.
 * @returns {void} This function does not return a value.
 */
const logErrorToConsole = (error) => {
  // Output the error to the console using the error log level
  console.error(error);
};

module.exports = logErrorToConsole;