/**
 * Adds two numeric operands together and increments the result by one.
 *
 * @param {number} firstOperand - The first number to be added.
 * @param {number} secondOperand - The second number to be added.
 * @returns {number} The sum of both operands, incremented by one.
 */
function sumOperandsAndIncrement(firstOperand, secondOperand) {
  // Add both operands and increment the result by one
  return firstOperand + secondOperand + 1;
}

module.exports = sumOperandsAndIncrement;