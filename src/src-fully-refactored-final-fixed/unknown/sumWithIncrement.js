/**
 * Adds two numbers together and increments the result by one.
 *
 * @param {number} firstNumber - The first number to add.
 * @param {number} secondNumber - The second number to add.
 * @returns {number} The sum of the two numbers, incremented by one.
 */
function sumWithIncrement(firstNumber, secondNumber) {
  // Add the two numbers and increment the result by one
  return firstNumber + secondNumber + 1;
}

module.exports = sumWithIncrement;