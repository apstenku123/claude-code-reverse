/**
 * Determines if the first value is less than or equal to the second value.
 *
 * @param {number} firstValue - The first value to compare.
 * @param {number} secondValue - The second value to compare against.
 * @returns {boolean} True if firstValue is less than or equal to secondValue, otherwise false.
 */
function isFirstValueLessThanOrEqualToSecond(firstValue, secondValue) {
  // Compare the two values and return the result
  return firstValue <= secondValue;
}

module.exports = isFirstValueLessThanOrEqualToSecond;